    private void isLoggedOut() {
        Timer timer = new Timer(1500, e -> {
            this.checkAccStatus();
        });
        timer.start();
    }

    public void checkAccStatus() {
        boolean status = GlobalState.accountStatus;
        if (status == false) {
            this.dispose();
        }
    }
