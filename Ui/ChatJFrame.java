    public void ketNoiServer() {
        try {
            s = new Socket("192.168.1.14", 12345);
            is = s.getInputStream();
            br = new BufferedReader(new InputStreamReader(is));
            os = s.getOutputStream();
            ps = new PrintStream(os);

            new Thread(() -> {
                String msg = "";
                try {
                    while ((msg = br.readLine()) != null) {
                        System.out.println("Chat Frame" + msg);
                        txtaTinNhan.append("\n"+msg);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }).start();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

