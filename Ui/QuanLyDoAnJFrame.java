    void loadData(int danhMuc) {
        model.setRowCount(0);
        danhSachDoAn = dao.danhSachSanPham(danhMuc);
        for (var doAn : danhSachDoAn) {
            model.addRow(new Object[]{
                    doAn.getTen_sp(),
                    PriceUtils.formatCurrency(doAn.getGia_sp())
            });
        }
        if (danhMuc == 1) {
            tblDoUong.setModel(model);
        } else {
            tblDoAn.setModel(model);
        }
    }

