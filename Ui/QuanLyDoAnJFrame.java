    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (modelDoAn == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm để xóa");
            return;
        }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa sản phẩm này?", "Xác nhận xóa", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            var rs = dao.xoaSanPham(modelDoAn.getId_san_pham());
            if (rs > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công");
                txtTen.setText("");
                txtGia.setText("");
                cboDanhMuc.setSelectedIndex(0);
                loadData(1); // Reload data based on the selected category
                modelDoAn = null;
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        var ten = txtTen.getText().trim(); 
        var gia = txtGia.getText().trim();
        var idDanhMuc = Integer.parseInt((String) Objects.requireNonNullElse(cboDanhMuc.getSelectedItem(), "0"));
        if (modelDoAn == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm để sửa");
            return;
        }
        if (ten.isEmpty() || gia.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
            return;
        }
        int giaInt;
        try {
            giaInt = Integer.parseInt(gia);
            if (giaInt <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Giá phải lớn hơn 0");
                return;
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Giá phải là một số nguyên dương");
            return;
        }
        modelDoAn.setTen_sp(ten);
        modelDoAn.setId_danh_muc(idDanhMuc);
        modelDoAn.setGia_sp(giaInt);
        var rs = dao.SuaSanPham(modelDoAn);

        if (rs > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Sửa thành công");
            txtTen.setText("");
            txtGia.setText("");
            cboDanhMuc.setSelectedIndex(0);
            loadData(idDanhMuc); // Reload data based on the selected category
            modelDoAn = null;
            txtTen.setText("");
            txtGia.setText("");
            cboDanhMuc.setSelectedIndex(0);
            loadData(idDanhMuc);
        } else if (rs == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Tên sản phẩm đã tồn tại");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Sửa thất bại");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

