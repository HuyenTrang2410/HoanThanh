    private void btnDoiMkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoiMkActionPerformed
        // TODO add your handling code here:
    String matKhauCu = new String(txtMatKhauCu.getPassword());
    String matKhauMoi = new String(txtMatKhauMoi.getPassword());
    String xacNhanMk = new String(txtXacNhan.getPassword());

    // 1️⃣ Kiểm tra trống
    if (matKhauCu.isEmpty() || matKhauMoi.isEmpty() || xacNhanMk.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
        return;
    }

    // 2️⃣ Kiểm tra mật khẩu cũ có đúng không
    if (!kiemTraMatKhauCu(matKhauCu)) {
        JOptionPane.showMessageDialog(this, "Mật khẩu cũ không đúng!");
        return;
    }

    // 3️⃣ Kiểm tra mật khẩu mới và xác nhận khớp
    if (!matKhauMoi.equals(xacNhanMk)) {
        JOptionPane.showMessageDialog(this, "Mật khẩu mới và xác nhận không khớp!");
        return;
    }

    // 4️⃣ Cập nhật mật khẩu trong DB
    if (capNhatMatKhauMoi(matKhauMoi)) {
        JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công!");
        this.dispose(); // Đóng dialog
    } else {
        JOptionPane.showMessageDialog(this, "Lỗi khi đổi mật khẩu!");
    }
    }//GEN-LAST:event_btnDoiMkActionPerformed

