    private boolean kiemTraMatKhauCu(String matKhauCu) {
    try (Connection con = KetNoiDB.getConnection()) {
        String sql = "SELECT * FROM Tai_Khoan WHERE ten_dang_nhap=? AND mat_khau=?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, Auth.currentUserTenDangNhap);
        ps.setString(2, matKhauCu);
        ResultSet rs = ps.executeQuery();
        return rs.next(); // Nếu có kết quả => mật khẩu cũ đúng
    } catch (Exception e) {
        e.printStackTrace();
    }
    return false;
}

// Cập nhật mật khẩu mới
private boolean capNhatMatKhauMoi(String matKhauMoi) {
    try (Connection con = KetNoiDB.getConnection()) {
        String sql = "UPDATE Tai_Khoan SET mat_khau=? WHERE ten_dang_nhap=?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, matKhauMoi);
        ps.setString(2, Auth.currentUserTenDangNhap);
        return ps.executeUpdate() > 0;
    } catch (Exception e) {
        e.printStackTrace();
    }
    return false;
}

