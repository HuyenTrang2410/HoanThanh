    private void batDauDemNguoc() {
        TaiKhoanDAO tkD = new TaiKhoanDAO();
        javax.swing.Timer timer = new javax.swing.Timer(1000, e -> {
            if (thoiGianConLai > 0) {
                thoiGianConLai--;
                soDu -= giaGio / 3600; // Tr·ª´ ti·ªÅn m·ªói gi√¢y

                txtThoiGian.setText(formatTime(thoiGianConLai));
                txtSoDu.setText(String.format("%.0f VND", soDu));

                // ‚úÖ N·∫øu s·ªë d∆∞ <= 1 th√¨ t·ª± ƒë·ªông ƒëƒÉng xu·∫•t v√† c·∫≠p nh·∫≠t s·ªë d∆∞
                if (soDu <= 1) {
                    tkD.updateStatus(Auth.currentUserTenDangNhap);
                    System.out.println("Current USername: " + Auth.currentUserTenDangNhap);
                    ((javax.swing.Timer) e.getSource()).stop(); // D·ª´ng timer

                    // üîÅ C·∫≠p nh·∫≠t s·ªë d∆∞ v√†o DB gi·ªëng btnDangXuat
                    try (Connection con = KetNoiDB.getConnection()) {
                        String sql = "UPDATE Tai_Khoan SET so_du = ? WHERE ten_dang_nhap = ?";
                        try (PreparedStatement ps = con.prepareStatement(sql)) {
                            ps.setDouble(1, soDu);
                            ps.setString(2, Auth.currentUserTenDangNhap);
                            int rows = ps.executeUpdate();
                            if (rows > 0) {
                                System.out.println("‚úÖ C·∫≠p nh·∫≠t s·ªë d∆∞ th√†nh c√¥ng (T·ª∞ ƒê·ªòNG ƒêƒÇNG XU·∫§T)");
                            }
                        }
                    } catch (SQLException ex) {
                        System.err.println("‚ùå L·ªói khi c·∫≠p nh·∫≠t s·ªë d∆∞ (T·ª∞ ƒê·ªòNG):");
                        ex.printStackTrace();
                    } catch (Exception exp) {
                        System.out.println("Error : " + exp);
                    }

                    JOptionPane.showMessageDialog(this, "S·ªë d∆∞ kh√¥ng ƒë·ªß! T·ª± ƒë·ªông ƒëƒÉng xu·∫•t.");

                    this.dispose(); // ƒê√≥ng form hi·ªán t·∫°i

                    // Reset Auth
                    Auth.currentUserTenDangNhap = null;
                    Auth.currentUserVaiTro = null;
                    Auth.currentUserSoDu = 0;

                    // M·ªü l·∫°i form ƒëƒÉng nh·∫≠p
                    new DangNhapJFrame().setVisible(true);
                    return;
                }

            } else {
                ((javax.swing.Timer) e.getSource()).stop();
                JOptionPane.showMessageDialog(this, "H·∫øt gi·ªù! Vui l√≤ng n·∫°p th√™m ti·ªÅn.");
                this.dispose();
                new DangNhapJFrame().setVisible(true); // ƒêƒÉng xu·∫•t v·ªÅ login
            }
        });
        timer.start();
    }

    private String formatTime(int seconds) {
        int h = seconds / 3600;
        int m = (seconds % 3600) / 60;
        int s = seconds % 60;
        return String.format("%02d:%02d:%02d", h, m, s);
    }

