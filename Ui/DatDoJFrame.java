    void loadChiTietDonHang() {
        chiTietModel.setRowCount(0);
        long tongTien = 0;
        for (var chiTiet : danhSachChiTietDonHang) {
            chiTietModel.addRow(new Object[]{
                    chiTiet.getId_san_pham(),
                    chiTiet.getTen_sp(),
                    chiTiet.getSoLuong(),
                    PriceUtils.formatCurrency(chiTiet.getTongTien())
            });
            tongTien += chiTiet.getTongTien();
        }
        tblDanhSach.setModel(chiTietModel);

        txtTongTien.setText(PriceUtils.formatCurrency(tongTien));
    }

    void loadSanPham(int danhMuc) {
        doAnModel.setRowCount(0);
        danhSachDoAn = doAnDAO.danhSachSanPham(danhMuc);
        for (var doAn : danhSachDoAn) {
            doAnModel.addRow(new Object[]{
                    doAn.getTen_sp(),
                    PriceUtils.formatCurrency(doAn.getGia_sp())
            });
        }
        if (danhMuc == 1) {
            tblDoUong.setModel(doAnModel);
        } else {
            tblDoAn.setModel(doAnModel);
        }
    }

