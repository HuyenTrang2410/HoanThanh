    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        var soLuong = Integer.parseInt(txtSoLuong.getText());
        if (soLuong <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0", "Thông báo", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (modelDoAn == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn món ăn hoặc đồ uống", "Thông báo", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        var chiTietDonHang = new ChiTietDonHangDTO(modelDoAn.getId_san_pham(), modelDoAn.getTen_sp(), soLuong, modelDoAn.getGia_sp(), modelDoAn.getId_danh_muc());

        var existingChiTiet = danhSachChiTietDonHang.stream()
                .filter(ct -> ct.getId_san_pham() == chiTietDonHang.getId_san_pham())
                .findFirst();
        if (existingChiTiet.isPresent()) {
            // Nếu món ăn đã tồn tại, cập nhật số lượng và tổng giá
            ChiTietDonHangDTO existing = existingChiTiet.get();
            existing.setSoLuong(existing.getSoLuong() + chiTietDonHang.getSoLuong());
            existing.setTongTien((long) existing.getSoLuong() * existing.getGia_sp());
        } else {
            // Nếu món ăn chưa tồn tại, thêm mới
            chiTietDonHang.setTongTien((long) chiTietDonHang.getSoLuong() * chiTietDonHang.getGia_sp());
            chiTietDonHang.setDoAn(new DoAn(modelDoAn));
            danhSachChiTietDonHang.add(chiTietDonHang);
        }
        loadChiTietDonHang();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (modelDoAn == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn món ăn hoặc đồ uống", "Thông báo", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        danhSachChiTietDonHang.stream()
                .filter(ct -> ct.getId_san_pham() == modelDoAn.getId_san_pham())
                .findFirst()
                .ifPresent(ct -> {
                    danhSachChiTietDonHang.remove(ct);
                    loadChiTietDonHang();
                    txtTenMon.setText("");
                    txtGia.setText("");
                    txtSoLuong.setText("1");
                    modelDoAn = null;
                });
    }//GEN-LAST:event_btnXoaActionPerformed

